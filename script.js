let currentLanguage = (typeof localStorage !== 'undefined' && localStorage.language === 'de') ? 'de' : 'en';

function applyTranslations() {
    const lang = currentLanguage in translations ? currentLanguage : 'en';
    const dict = translations[lang];

    document.documentElement.setAttribute('lang', lang);

    const metaTitle = dict['meta.title'];
    if (metaTitle) {
        document.title = metaTitle;
    }

    const metaDesc = dict['meta.description'];
    const metaDescEl = document.querySelector('meta[name="description"]');
    if (metaDesc && metaDescEl) {
        metaDescEl.setAttribute('content', metaDesc);
    }

    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const key = el.getAttribute('data-i18n');
        const value = dict[key];
        if (!value) return;

        const tag = el.tagName.toLowerCase();
        if (tag === 'input' || tag === 'textarea') {
            el.placeholder = value;
        } else {
            el.textContent = value;
        }
    });
}

function updateLanguageToggleUI() {
    document.querySelectorAll('[data-lang-btn]').forEach((btn) => {
        const lang = btn.getAttribute('data-lang-btn');
        const isActive = lang === currentLanguage;

        btn.classList.remove('bg-gray-900', 'text-white', 'dark:bg-white', 'dark:text-gray-900');
        btn.classList.remove('bg-transparent', 'text-gray-700', 'dark:text-gray-300');

        if (isActive) {
            btn.classList.add('bg-gray-900', 'text-white', 'dark:bg-white', 'dark:text-gray-900');
            btn.setAttribute('aria-pressed', 'true');
        } else {
            btn.classList.add('bg-transparent', 'text-gray-700', 'dark:text-gray-300');
            btn.setAttribute('aria-pressed', 'false');
        }
    });
}

function setLanguage(lang) {
    if (!(lang in translations)) return;
    currentLanguage = lang;
    if (typeof localStorage !== 'undefined') {
        localStorage.language = lang;
    }
    applyTranslations();
    updateLanguageToggleUI();
}

document.addEventListener('DOMContentLoaded', () => {
    // Theme initialization
    if (localStorage.theme === 'dark' || (!('theme' in localStorage) && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
        document.documentElement.classList.add('dark');
    } else {
        document.documentElement.classList.remove('dark');
    }

    // Apply initial language
    applyTranslations();
    updateLanguageToggleUI();

    // Smooth scroll for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const href = this.getAttribute('href');
            if (href !== '#' && href !== '#!') {
                e.preventDefault();
                const target = document.querySelector(href);
                if (target) {
                    const offsetTop = target.offsetTop - 80; // Account for fixed navbar
                    window.scrollTo({
                        top: offsetTop,
                        behavior: 'smooth'
                    });
                }
            }
        });
    });
});

const navbar = document.getElementById("navbar");
const navLink = document.getElementById("navLink");
const mobileMenu = document.getElementById("mobileMenu");
const menuButton = document.getElementById("menuButton");

function openMenu() {
    if (mobileMenu) {
        mobileMenu.style.transform = 'translateX(-16rem)';
        mobileMenu.style.visibility = 'visible';
        if (menuButton) {
            menuButton.setAttribute('aria-expanded', 'true');
        }
        // Prevent body scroll when menu is open
        document.body.style.overflow = 'hidden';
    }
}

function closeMenu() {
    if (mobileMenu) {
        mobileMenu.style.transform = 'translateX(0)';
        if (menuButton) {
            menuButton.setAttribute('aria-expanded', 'false');
        }
        // Restore body scroll
        document.body.style.overflow = '';
    }
}

function toggleTheme() {
    document.documentElement.classList.toggle('dark');

    if (document.documentElement.classList.contains('dark')) {
        localStorage.theme = 'dark';
    } else {
        localStorage.theme = 'light';
    }
}

// Close mobile menu when clicking outside
document.addEventListener('click', (e) => {
    if (mobileMenu && menuButton && !mobileMenu.contains(e.target) && !menuButton.contains(e.target)) {
        if (mobileMenu.style.transform === 'translateX(-18rem)') {
            closeMenu();
        }
    }
});

// Close mobile menu on escape key
document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && mobileMenu && mobileMenu.style.transform === 'translateX(-18rem)') {
        closeMenu();
        if (menuButton) {
            menuButton.focus();
        }
    }
});

// Navbar scroll effect
let lastScroll = 0;
window.addEventListener('scroll', () => {
    const currentScroll = window.pageYOffset || document.documentElement.scrollTop;
    
    if (currentScroll > 50) {
        navbar.classList.add('bg-white', 'bg-opacity-80', 'backdrop-blur-lg', 'shadow-md', 'dark:bg-darkTheme', 'dark:shadow-white/20');
        navLink.classList.remove('bg-white', 'shadow-sm', 'bg-opacity-50', 'dark:border', 'dark:border-white/30', "dark:bg-transparent");
    } else {
        navbar.classList.remove('bg-white', 'bg-opacity-80', 'backdrop-blur-lg', 'shadow-md', 'dark:bg-darkTheme', 'dark:shadow-white/20');
        navLink.classList.add('bg-white', 'shadow-sm', 'bg-opacity-50', 'dark:border', 'dark:border-white/30', "dark:bg-transparent");
    }
    
    lastScroll = currentScroll;
});

// Form submission handler (with i18n)
const contactForm = document.querySelector('#contact form');
if (contactForm) {
    contactForm.addEventListener('submit', (e) => {
        e.preventDefault();
        const submitButton = contactForm.querySelector('button[type="submit"]');
        const submitTextSpan = submitButton ? submitButton.querySelector('[data-i18n="contact.submit"]') : null;

        if (submitTextSpan) {
            const successText = translations[currentLanguage]?.['contact.submit_success'] || 'Message sent! ✓';
            submitTextSpan.textContent = successText;
        }

        submitButton.disabled = true;

        setTimeout(() => {
            if (submitTextSpan) {
                const defaultText = translations[currentLanguage]?.['contact.submit'] || 'Submit now';
                submitTextSpan.textContent = defaultText;
            }
            submitButton.disabled = false;
            contactForm.reset();
        }, 3000);
    });
}